  final int quarteirao;
  final String data;
  final String horas;

  const Dados(
      {required this.SIAT,
      required this.nome,
      required this.cpf_cnpj,
      required this.rua,
      required this.numero_casa,
      required this.quarteirao,
      required this.data,
      required this.horas});

  Dados copy({
    int? SIAT,
    String? nome,
    String? cpf_cnpj,
    String? rua,
    String? numero_casa,
    int? quarteirao,
    String? data,
    String? horas,
  }) =>
      Dados(
        SIAT: SIAT ?? this.SIAT,
        nome: nome ?? this.nome,
        cpf_cnpj: cpf_cnpj ?? this.cpf_cnpj,
        rua: rua ?? this.rua,
        numero_casa: numero_casa ?? this.numero_casa,
        quarteirao: quarteirao ?? this.quarteirao,
        data: data ?? this.data,
        horas: horas ?? this.horas,
      );
}

